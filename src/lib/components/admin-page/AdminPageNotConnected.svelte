  <p>
    You need to create a ProgNovel project in your computer to use ProgNovel admin.<br />To do so,
    read this instruction:
  </p>
  <ol>
    <li>
      Install NodeJS in your computer if you haven't already. Download link: <a
        href="/https://nodejs.org/en/">https://nodejs.org</a
      >
    </li>
    <li>
      Open Command Prompt or Terminal of your OS. Install ProgNovel CLI (Command Line Interface) by
      running <code>npm install @prognovel/cli -g</code>
    </li>
    <li>
      In Command Prompt, <code>cd</code> to an empty folder you want to be your ProgNovel project
      folder. Or if you don't know how to navigate inside Command Prompt, simply the empty project
      folder with Explorer and type <code>cmd</code> in Explorer's location bar.
    </li>
    <li>
      Once Command Prompt shows your empty project folder as working directory, enter <code
        >prognovel new</code
      >
    </li>
    <li>
      You will have a fresh ProgNovel project in your folder. You can manually edit configurations
      or enter command <code>prognovel gui</code> to enable admin dashboard in any ProgNovel sites.
    </li>
  </ol>
</article>

